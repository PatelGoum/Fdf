#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	if (!lst)
		return (NULL);
	ptr = lst->next;
	while (ptr)
	{
		ptr = ptr->next;
		lst = lst->next;
	}
	return (lst);
}
